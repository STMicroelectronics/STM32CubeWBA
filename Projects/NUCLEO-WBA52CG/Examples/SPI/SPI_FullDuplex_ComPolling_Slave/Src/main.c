      BSP_LED_On(LD1);
      /* Turn LD2 on: Transfer in reception process is correct */
      BSP_LED_On(LD2);
      break;

    case HAL_TIMEOUT:
      /* A Timeout Occur ______________________________________________________*/
      /* Call Timeout Handler */
      Timeout_Error_Handler();
      break;
