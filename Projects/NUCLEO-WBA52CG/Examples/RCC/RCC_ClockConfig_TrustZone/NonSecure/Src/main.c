  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    if (UserButtonPressed == 1U)
    {
      if (__HAL_RCC_GET_PLL1_OSCSOURCE() == RCC_PLLSOURCE_HSI)
      {
        /* PLL source is HSI oscillator */
        /* Set SYSCLK frequency to 100000000 Hz, coming from the PLL which is clocked by HSE  */
        SECURE_SystemClockHSE_Config();

        ToggleDelay = FAST_DELAY;  /* Apply fast LD2 toggling */
      }
      else if (__HAL_RCC_GET_PLL1_OSCSOURCE() == RCC_PLLSOURCE_HSE)
      {
        /* PLL source is HSE  oscillator */
        /* Set SYSCLK frequency to 100000000 Hz, coming from the PLL which is clocked by HSI */
        SECURE_SystemClockHSI_Config();

        ToggleDelay = SLOW_DELAY;  /* Apply slow LD2 toggling */
      }
      else
      {
        /* Do nothing as not expected SystemCoreClock value */
      }

      UserButtonPressed = 0U;
    }

    /* Toggle LD2 */
    HAL_GPIO_TogglePin(LD2_GPIO_Port, LD2_Pin);
    HAL_Delay(ToggleDelay);
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
